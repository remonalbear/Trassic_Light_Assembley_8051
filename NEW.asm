		TABLE EQU 100H
		ORG 0
		MOV A, #0FFH
		MOV P2, A
		MOV R6, #32
		MOV R5, #03H
		SETB P2.5
		CLR P2.4
MAIN:	MOV A, R5
		MOV DPTR, #TABLE
		MOV R1, A
      	MOV R0, #00H
LOOP: 	MOV A, R0														 
		MOVC A, @A+DPTR
	 	MOV P3, A
		MOV A,R1
		MOVC A, @A+DPTR
		MOV P1, A
		MOV A, R6
	  	MOV R2, A
	  	MOV R3, #10
	  	MOV R4, #0
	  	ACALL DELAY
		DEC R0
		CJNE R0, #0FFH, LOOP	  	
SUB1:	DEC R1
		MOV R0, #09H
		CJNE R1,#0FFH, LOOP
		CPL P2.5
		CPL P2.4
		JMP MAIN
SBT:	DEC	R5
		MOV R3, #0
		MOV R4, #0
		ACALL DELAY2
		ACALL DELAY2
		JMP MAIN
DVT:	MOV A, R6
		MOV B, #02
		DIV AB
		MOV R6, A
		MOV R3, #0
		MOV R4, #0
		ACALL DELAY2
		ACALL DELAY2
		RET
MTV:	MOV A, R6
		MOV B, #02
		MUL AB
		MOV R6, A
		MOV R3, #0
		MOV R4, #0
		ACALL DELAY2
		ACALL DELAY2
		RET
ADR:	INC	R5
		MOV R3, #0
		MOV R4, #0
		ACALL DELAY2
		ACALL DELAY2
		JMP MAIN
DELAY: 	JNB P2.0, ADR
		JNB P2.1, SBT
		JNB P2.2, MTV
		JNB P2.3, DVT
		JNB P2.6, RST
		DJNZ R4, DELAY
	   	DJNZ R3, DELAY
		MOV R3,#10 
	   	DJNZ R2, DELAY
		RET
RST:	MOV R5, #03H
		MOV R6, #32
		MOV A, #0FFH
		MOV P2, A
		SETB P2.5
		CLR P2.4
		JMP MAIN																											
DELAY2:	DJNZ R3, DELAY2
		DJNZ R4, DELAY2
		RET
